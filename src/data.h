#ifndef DATA_H
#define DATA_H

/*
 * Data-oriented macros.
 */
#define kB 1000         // Kilobyte
#define MB 1000000      // Megabyte
#define GB 1000000000   // Gigabyte
#define B_TO_KB(B) B/kB // Converts bytes to kilobytes
#define B_TO_MB(B) B/MB // Converts bytes to megabytes
#define B_TO_GB(B) B/GB // Converts bytes to gigabytes

#endif /* DATA_H_ */
